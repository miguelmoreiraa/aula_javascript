// Igual(==)
// Retorna verdadeiro caso os operandos sejam iguais. 3 == var1
"3" == var1
3 == '3'

// Não igual(!=)
// Retorna verdadeiro caso os operandos não sejam iguais. var1 != 4
var2 != "3"

// Estritamente igual(===)
// Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo. Veja também Object.is e igualdade
3 === var1

// Estritamente não igual(!==)
// Retorna verdadeiro caso os operandos não sejam iguais e/ou não sejam do mesmo tipo.
var1 !== "3"
3 !== "3"

// Maior que(>)
// Retorna verdadeiro caso os operandos da esquerda seja maior que o da direita
var1 > var2

    "12" > 2

// Maior que(>=)
// Retorna verdadeiro caso os operandos da esquerda seja maior ou igual ao da direita
var1 >= var2
var1 >= 3

// Maior que(>)
// Retorna verdadeiro caso os operandos da esquerda seja menor que o da direita
var1 < var2

    "12" > 2

// Maior que(<=)
// Retorna verdadeiro caso os operandos da esquerda seja maior ou igual ao da direita
var1 <= var2
var1 <= 5